// Iterate with a for loop. For loops are the most common type of loops in JS. Once the condition in the for-loop evaluates to false
// we break out of the loop.

let ourArray = [];

for (let i = 0; i < 5; i++) {
  ourArray.push(i);
}

console.log(ourArray);

let myArray = [];

for (let i = 1; i < 6; i++) {
  myArray.push(i);
}

console.log(myArray);
